      <h2>Contents: Series and Texts Entered</h2>

      <p>The following is a list of series and publishers whose works have been
entered into the database.</p>

	<p>Jump to: <a href="#printed">Printed Sources</a> |  <a href="#online">Online Sources</a>

      <a name="printed"></a><h3>Printed Sources</h3>

<p><b>Anglo-Norman Text Society</b><br />
1939 - . Scholarly editions of texts in Anglo-Norman.  All volumes published up to 2006 are in OMSB, as are the 16 volumes in the Plain Texts Series.</p>

<p><b>Anglo-Saxon Texts</b><br>
Boydell and Brewer series, 1999 - . Editions of Anglo-Saxon texts. 4 of 9 texts published in OMSB.</p>

<p><b>Canterbury and York Society</b><br>
Published by the Society, 1907-1989, and by Boydell and Brewer since 1990. English ecclesiastical records, primarily bishopâ€™s registers. All but 14 of the nearly 100 medieval volumes are in OMSB.</p>

<p><b>Cistercian Fathers Series</b><br>
Cistercian Publications, 1970 - . English translations of texts by major Cistercian monks. 18 of the c. 60 medieval primary texts in the series are entered in OMSB.</p>

<p><b>Cistercian Studies Series</b><br>
Cistercian Publications, 1969 - . The series includes about 70 (of a total 204 published volumes) containing English translations of a wide variety of medieval monastic texts; 18 are in  OMSB. </p>

<p><b>Classics of Western Spirituality</b><br />
Paulist Press, 1978 - .  Translations of classic texts from the Christian, Jewish, and Muslim traditions, mostly medieval. 84 volumes, 18 in OMSB as of 2012.</p>

<p><b>Corpus Christianorum Continuatio Medieavalis</b><br />
Brepols, 1966 - . Christian texts from the Carolingian period to the end of the middle ages. About 135 of over 260 texts are in OMSB.</p>

<p><b>Crusade Texts in Translation</b><br />
Ashgate Press, 1996 - . English translations of sources (mainly chronicles) about the crusades. 14 of 25 volumes are in OMSB.</p>

<p><b>Early English Text Society Original Series</b><br>
Oxford University Press and Boydell and Brewer for the Early English Text Society, 1864 - . Critical editions of texts in Old and Middle English. Over 200 texts of 475 volumes published are in OMSB.</p>

<p><b>Henry Bradshaw Society</b><br>
Now published by Boydell and Brewer, 1891- . Editions and facsimiles of British liturgical documents, the majority of them medieval. Volumes 1 (1891) through 30 (1905) are in OMSB.</p>

<p><b>The I Tatti Renaissance Library</b><br>
Harvard University Press, 2001 - .. Editions of Latin texts written during the Italian Renaissance with facing-page English translations. 17 of the 52 volumes published as of 2012 are in OMSB.</p>

<p><b>Irish Texts Society</b><br>
1899 - .  Publishes editions and English translations of Irish philological works, dictionaries, and primary sources, some in translation. 8 of about 40 medieval volumes are in OMSB.</p>

<p><b>Library of Medieval Women</b><br>
Boydell and Brewer, 1997-. Short translations of texts wrtiten by or women from the Patristic period into the sixteenth century.  13 of the 26 volumes published by 2012 are in OMSB.</p>

<p><b>Manchester Medieval Sources Series</b><br>
Macmillan Press, 1994 - .   English translations of excerpts from a wide variety of texts and documents centered around a particular theme with long  introductions and explanatory material. Available online by subscription about five years after publication. 15 of 22 volumes are in OMSB as of 2012.</p>

<p><b>Mediaeval and Modern Irish Series</b><br>
Begun by the Irish Stationery Office in 1931, continued by the Irish Manuscripts Commission and now published by the Dublin Institute for Advanced Studies. Publications include medieval Irish texts, few of which have been translated into English; about 10 of the c. 25 medieval texts in the series are in OMSB.</p>

<p><b>Medieval Chronicles</b><br>
Boydell and Brewer,  1999-. Scholarly editions, with English translations of non-English chronicles. 4 of 5 published as of 2012 in OMSB.</p>

<p><b>Medieval Texts and Translations</b><br>
Boydell and Brewer series, 1992-. 5 published; all in database.</p>

<p><b>Middle Ages Series</b><br>
University of Pennsylvania Press, 1972 - . Contains documentary collections on specific themes, as well as monographs. All 32 volumes of medieval texts published in 1972-96 have been entered in OMSB. </p>

<p><b>Monumenta Germaniae Historica</b><br />
1826-. Sources for the study of German history from the end of the Roman Empire to roughly 1500. 69 texts are currently in the database.</p>

<p><b>Oxford Medieval Texts</b><br>
Oxford University Press, 1967 - . Modern editions, usually with translations. Publishes mostly chronicles and saints lives; 64 of 72 volumes are in OMSB.</p>

<p><b>Penguin Classics</b><br>
Penguin Press, ~1940 -  . Translations of a wide variety of texts and records. Medieval titles are numerous; about 92 have been entered into OMSB. </p>

<p><b>Readings in Medieval Civilizations and Cultures</b><br>
University of Toronto Press, originally Broadview Press, 2003 - . Translations, mostly of excerpts on particular themes. 8 of 15 volumes are in OMSB.</p>

<p><b>Records of Civilization, Sources and Studies</b><br>
Columbia University Press, 1915-. Many but not all are medieval; many
published in Norton paperback editions, and the contents of many which
went out of copyright are available online on the <a
href="http://www.fordham.edu/halsall/sbook.html">Internet Medieval
Sourcebook</a>. All 24 of the medieval volumes are in the database.</p>

<p><b>Records of Early English Drama</b><br>
Centre for Research in Early English Drama, 1979 - . Published by University of Toronto Press and (since 2002) jointly with the British Library, now Boydell and Brewer.  REED examines the historical manuscripts that provide external evidence of drama, secular music, and other communal entertainment and ceremony from the Middle Ages until 1642. As of 2012, 21titles are in OMSB; 29 volumes are available as pdf files through the Internet Archive.</p>

<p><b>Rerum Italicarum Scriptores</b><br />
Writings on Italian history from 500 to 1500, collected in many volumes: 28 edited by L.M. Muratori in 1723-51, with others published from 1900 on.  Many now available online. OMSB includes about 25 works.</p>

<p><b>Rolls Series</b><br>
Published by the British Government under the official title "The Chronicles and Memorials of Great Britain and Ireland during the Middle Ages," c.1850-1900. Published  chronicles and other historiographical works relating to British history, as well as some literary and hagiographical works and documentary evidence. All 99 texts, contained in over 200 volumes, are in OMSB. Many are now available online.</p>

<p><b>Selden Society</b><br>
c.1890-. The Selden Society is devoted to the study of English legal history, and publishes British legal documents. As of 2012, about 98 of 120 medieval volumes are in OMSB.</p>

<p><b>Scriptores Latini Hiberniae</b><br>
Dublin Institute for Advanced Studies, 1955-1998, with more recent reprints. Latin texts written by medieval Irish writers or about medieval Ireland. 8 of the 14 volumes are in OMSB.</p>

<p><b>TEAMS Middle English Text Series</b><br>
1990- . Transcriptions of Middle English texts designed for classroom use and non-specialists, available as books or online.  As of 2012, OMSB includes 280 of the 300 published volumes.</p>

<p><b>Textos Medievales</b><br />
Anubar Press. Editions of Spanish (including Catalan) medieval texts and documents. As of 2012, about 25 of 93 volumes in OMSB.</p>

<p><b>Traditio</b><br>
Fordham University Press, 1945-. Journal of ancient and medieval history which frequently includes editions of medieval texts. All relevant texts published in 1946-1948 are in OMSB.</p>

<p><b>Translated Texts for Historians</b><br>
Liverpool University Press, 1985-. 43 texts published, at least half are medieval; 28 published 1988-2002 are entered in OMSB.</p>

<p><b>Translations and Reprints of the Original Sources of European History</b><br>
Published for the Dept. of History, University of Pennsylvania, 1894-1907. 21 medieval selections in 7 volumes are in OMSB; most also available online via Google Books or the Internet Medieval Sourcebook</p>




<a name="online"></a><h3>Online Sources</h3>
<p>The following websites contain many primary sources, all of which have been entered separately into the database.  In 
addition to these, dozens of websites containing one or a few texts are also in the database.</p>

<p><b><a href="http://www.british-history.ac.uk/">British History 
Online</a></b><br />
A digital library containing some of the core printed primary and secondary sources for the medieval and modern history of the British Isles. OMSB includes roughly 30 of the medieval texts.</p>

<p><b><a href="http://www.ucc.ie/celt/index.html">CELT (The Corpus of
Electronic Texts)</a></b><br>A website of Irish texts in original and in translation, maintained by the University College, Cork, Ireland. 24 texts (of c. 100 medieval texts) are in the database.</p>

<p><b><a href="http://www.hti.umich.edu/c/cme/bibl.html">Corpus of Middle
English Prose and Verse</a></b><br>Most of the Middle English works on this site, maintained by the University of Michigan, are electronic editions of public domain volumes of the Early English Text Society. Contains 60 texts in Middle English, all of which are in the database.
</p>

<p><b><a href="http://etext.lib.virginia.edu/uvaonline.html">Electronic Text
Center at the University of Virginia</a></b><br> The Electronic Text Center has tens of thousands of texts from many countries and time periods. There are roughly 26 texts in Middle English available to the general public on the website, all of which are in the database.</p>

<p><b><a href="http://www.yorku.ca/inpar/">In Parenthesis</a></b><br>
Maintained at the University of York in Toronto, Canada, In Parenthesis makes available PDF documents of out-of-copyright translations and editions of a wide variety of texts and language learning tools. Roughly 40 medieval texts are available (many for Records of Early English Drama), all of which are in the database.</p>

<p><b><a href="http://www.fordham.edu/halsall/sbook.html">Internet Medieval
Sourcebook</a></b><br> Maintained by Paul Halsall at Fordham University. Contains hundreds of online editions of texts, as well as links to other websites of primary sources. Roughly 65 texts are in the database.</p>

<p><b><a
href="http://www.georgetown.edu/labyrinth/library/library.html">Labyrinth
Library</a></b><br> Includes texts in original languages and translation. The most useful texts are those in Old English. 8 texts are in the database.</p>

<p><b><a href="http://www.trytel.com/~tristan/towns/towns.html">Medieval 
English Urban History</a></b><br />A website created by Stephen Alsford that illuminates all aspects of life in medieval English towns. Includes dozens of selections on a wide range of topics related to urban history; the longer documents are in OMSBy.</p>

<p><b><a
href="http://www.leeds.ac.uk/history/weblearning/MedievalHistoryTextCentre/medievalTexts.htm">Leeds
Medieval History Texts in Translation</a></b><br>Maintained by Graham Loud at Leeds University, this website contains the first published English translations of texts relating to the Pontificate of Gregory VII, the Crusades, and the Norman Kingdom of Sicily. Roughly 20 texts are available, all of which are in the database.</p>

<p><b><a href="http://monasticmatrix.org/">Monastic Matrix</a></b><br />
A scholarly resource for the study of women's religious communities from 400 to 1600. Includes a collection of over 30 primary sources, all of which are in the database.</p>

<p><b><a href="http://www.cyberscotia.com/ogmios/main-books.html">Ogmios
Press</a></b><br> Ogmios Press is an online publishing house offering free access to a catalogue of historical texts. They specialise in Scottish and Celtic subjects, publishing first online editions of significant or otherwise intriguing texts. All of their medieval texts are in the database.</p>

<p><b><a href="http://sunsite.berkeley.edu/OMACL/">OMACL: Online Medieval
and Classical Library</a></b><br> Maintained by the University of California at Berkeley. Contains roughly 40 medieval texts in translation, all of which are in the database.</p>

<p><b><a href="http://www.the-orb.net/libindex.html">ORB: Online Reference
Book for Medieval Studies</a></b><br>Contains many new transcriptions and translations of important medieval texts that have not been previously accessible in print or electronic format. Roughly 20 texts are in the database.</p>

<p><b><a href="http://www.gutenberg.org/">Project Gutenberg</a></b><br />
Begun in 1971 and maintained entirely by volunteers, Project Gutenberg is the oldest collection of digital texts. It contains thousands of full texts of public domain books, produced in simple formats that should outlast major technological changes. 63 medieval texts are in the database.</p>




