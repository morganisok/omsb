<h2>About the Online Medieval Sources Bibliography</h2>

<article id="about" class="left">
<p>This database provides a searchable bibliography of texts - from 
private letters, wills, and household accounts to literary works, 
philosophical treatises, chronicles, court proceedings, church records, 
and a host of other documents - that were written in the Middle Ages 
and are now available in printed or online editions and translations.  The 
aim is to provide annotated entries that include information on the genre, 
contents, archival reference and original language of the text, as well as 
whether the publication includes a translation, introduction, appendices, 
glossary, and index.  Where appropriate, the bibliography also includes 
hyperlinks to sources that are online.  </p>

<p>Begun in Summer 2003, this database was designed to record details 
about printed and online primary sources that can shed light on the 
history, literature, culture, and religion of the Middle Ages. In Phase I, 
we are focusing on primary sources that have been translated into English 
and are accessible in most college libraries or online. The database is 
currently weighted towards primary sources relating to England, France, 
and Ireland, but we are now adding more materials for other European 
countries. The primary sources included here date primarily from the 
period between roughly 300 and 1500, although there are sources that date 
from before and after these years.</p>
</article>

