<h2>Record Types</h2>
<table>
<tr><td>Account Roll</td><td>Document that records revenues and/or expenses.
Includes accounts not specified under the specific Account Roll
headings.</td></tr>

<tr><td>Account Roll - Bailiff/Reeve</td><td>Document that records payments made under the
bailiff's or reeve's (a local peasant chosen or elected to supervise the
agricultural work on the manor) authority in a manor or agricultural
settlement. </td></tr>

<tr><td>Account Roll - Building</td><td>Document that records expenses incurred in
construction of a building, particularly castles, town walls, churches,
and ships. </td></tr>

<tr><td>Account Roll - Estate</td><td>
Document that records the revenues and expenses of a manorial estate as a
whole (rather than just one part of the estate, such as a reeve's account
roll).  It usually includes the reeve's accounts from individual manors,
or other properties (such as fishponds, mines, forests, markets, and
fairs) owned by a lord.</td></tr>

<tr><td>Account Roll - Household</td><td>Document that records the expenses of a household.
Includes large royal, seigneurial, and ecclesiastical (monastic,
Episcopal) households, as well as accounts imbedded in <i>ricordi</i>
(Memoirs).</td></tr>

<tr><td>Account Roll - Obedientiary</td><td>Accounts produced by a monastic officer (such as the cellarer, sacristan, abbot, etc.) that 
details the revenues coming into his department (often from manors, in which case also add Account . 
Bailiff/Reeve or Account . Estate) and, more importantly, the expenses of this officer.</td></tr>

<tr><td>Account Roll - Port Customs</td><td>An account of port customs or tolls paid to the king,
lord, or port town listing the names of ships and their cargoes.</td></tr>

<tr><td>Bestiary</td><td>
A type of medieval compendium that describes the physical and allegorical qualities of (real or legendary) animals and birds (and sometimes rocks), often containing moralized lessons.  Frequently illustrated.</td></tr>


<tr><td>Biography</td><td>A narrative document describing the life of 
someone other than the author.  Does not include hagiography.<br
/><br /></td></tr>

<tr><td>Calendar</td><td>
A list organized by months and days within the year.  Most medieval calendars are liturgical in that they focus on the seasonal cycles of the Christian church and list commemorations of particular saints and holy days. Some also record lunar cycles and other astronomical features.  </td></tr>


<tr><td>Cartulary</td><td>A collection of charters which register the
acquisitions of a lord, monastery, or other institution, record successful
lawsuits, and otherwise document the institution's rights to its
properties.</td></tr>

<tr><td>Charters, Deeds</td><td>Documents recording grants and purchases, usually of
land, but sometimes of other property or rights.</td></tr>

<tr><td>Chronicle, Annals </td><td>Both of these are works of history.  Annals are
arranged by year, and comprise lists of events during each year.
Chronicles have a more narrative form.  Sometimes, medieval historical
works are more literary than factual, but if a work claims to be history,
it is put in this category.</td></tr>

<tr><td>Commentary / Gloss / Exegesis</td><td>A medieval text that provides commentary, glosses, or annotations on another text;
works discussing and expounding upon the meaning of specific biblical passages or books.</td></tr>

<tr><td>Contract</td><td>A binding agreement between two or more people or
entities, including contracts for construction, dowry, loans,
apprenticeship, etc.  Includes contracts in notarial registers.</td></tr>

<tr><td>Coroner's Roll</td><td>Records kept by a coroner about the deceased, the
cause of an individual's death, and any information witnesses of the death
could provide, including suspected murder and accidental deaths.</td></tr>

<tr><td>Councils - Church</td><td>Proceedings of a formal assembly of clergy; can include their discussions, legislation, and 
inquisitions, although these separate types of records, if present in sufficient numbers, should also be included 
in Record Type if this option is chosen.</td></tr>

<tr><td>Councils - Secular</td><td>Proceedings of a formal assembly of laity called together by a secular authority; can 
include their discussions, legislation, and inquisitions, as well as petitions to their assembly, although these 
separate types of records, if present in sufficient numbers, should also be included in Record Type if this 
option is chosen.</td></tr>

<tr><td>Court Roll</td><td>Records of court proceedings, usually recorded by a
clerk during court sessions. Includes government officials' investigations
of wrongdoing.</td></tr>

<tr><td>Court Roll - Ecclesiastical</td><td>Charges, depositions, and other records of litigation
in church courts governed by canon law (including marriage litigation,
defamation, clerical disputes, tithes, and even debt).<br /><br /></td></tr>

<tr><td>Court Roll - Eyre</td><td>Court rolls from eyre courts, which were royal
itinerant courts in pre-plague England.</td></tr>

<tr><td>Court Roll - Gaol Delivery</td><td>Court rolls recording indictments and trials of felons
in medieval England.<br><br></td></tr>

<tr><td>Court Roll - Sessions of the Peace</td><td>Court rolls from courts presided over by justices of
the peace in England, who adjudicated minor assaults and thefts,
trespasses, breaches of statute legislation (such as labor laws), and many
other cases.  The scope of their jurisdiction grew to include felonies and
more major crimes by the very end of the middle ages.</td></tr>

<tr><td>Custumal </td><td>A document which records "how things have always been
done."  Custom was often the basis for medieval laws and systems of
government, but as people saw a need for laws to be codified, they wrote
custumals to record what the laws have previously been in a village or
town.  Often includes information on inheritance, procedures for choosing
officials, bylaws on managing local agriculture, and services or taxes due
to the lord.  Most often used for manors, villages, and towns, but
there are also regional examples, such as the <i>Sachenspiegal</i>.</td></tr>

<tr><td>Dialogue</td><td>A classical genre used by many authors as an instructional technique, usually involving 
questions and answers between a pupil and teacher, but sometimes between two people on opposing sides of an 
issue. If the dialogue is philosophical in nature, then also add Philosophic Work as a Record Type.</td></tr>

<tr><td>Disputation - Philosophical/Theologicale</td><td>Texts written in format of an oral scholastic disputation (using the dialectical method of 
opposing arguments) on particular philosophical or theological questions which are furnished by participants, and 
where the master must come to a doctrinal solution by an act of determination which confirms him in his function 
as master.  Can include quodlibetal questions (quodlibet = any whatever) which were brought forward by students 
rather than masters and often reflect contemporary controversies.</td></tr>

<tr><td>Encyclopedia</td><td>
Text with information on many subjects, usually arranged alphabetically and sometimes thematically</td></tr>


<tr><td>Extents and Surveys </td><td>Surveys of tenements, field, and other properties and
rights, including ponds, rivers, woods, and markets owned by a lord or
estate.  Often have custumals attached.</td></tr>

<tr><td>Formulary</td><td>Collection of sample documents (charters, letters, contracts, account rolls) meant to serve 
as a model for clerks.  If the collection is largely made up of one type of document (e.g., letters), enter that 
Record Type as well.</td></tr>

<tr><td>Genealogy </td><td>Records of people's ancestry and lineage.</td></tr>

<tr><td>Glossary/Dictionary</td><td>Word list or translations/explanations of words into different languages, composed in the 
middle ages.</td></tr>

<tr><td>Guild Records</td><td>Any type of record generated by or about a formal trade or merchant guild, including their 
membership lists, charters, ordinances, court proceedings, and dealings with other guilds.  Does not include 
parish guilds or confraternities.</td></tr>

<tr><td>Hagiography</td><td>Includes several different types of sources, but
primarily narratives about the life of holy men and women written to
celebrate their devout life, ascetic practices, good deeds, and
miracles. Can be composed after someone was declared a saint, or to
help the canonization process along.  Includes martyrologies,
miracle stories, canonization proceedings (which can include
depositions from people who witnessed the divine intervention of the
person up for sainthood), descriptions of relics of or monuments to
particular saints, accounts of pilgrimages to holy sites (if the
focus is on the religious aspects of the pilgrimage), and devotional
tales about the intercession of the Virgin Mary.</td></tr>

<tr><td>Inquisition - Heresy</td><td>Records of inquisition trials (including depositions
from witnesses and accused person), in which a religious authority tries
people for heresy (when <span class=field>Record Type</span>: <i>Courts -
Ecclesiastical</i> is also used), and other inquisitorial records,
including handbooks for inquisitors (when <span class=field>Record
Type</span>: <i>Theology - Instructional</i> is also used) and
descriptions of heresy and witchcraft.</td></tr>

<tr><td>Inscription</td><td>Anything written or engraved on a surface other than
paper/parchment, such as a coin, medal, monuments, rock, seals, tree,
tombstone, furniture, etc.  Usually quite brief.</td></tr>

<tr><td>Inventory</td><td>A list of items in a place, such as a house or
monastery, or owned by an individual taken at a person's death, or because
of forfeiture.  Includes clothing, furniture, tools, and other
possessions.</td></tr>

<tr><td>Law - Canon Law</td><td>Includes church legislation promulgated in synods or
councils, large compilations of church law (such as Gratian), and any
legal process (petitions, depositions, church courts) that is adjudicated
by church law.</td></tr>

<tr><td>Law - Legislation</td><td>Laws promulgated by a king or state as statute
legislation, royal charter, or letters patent, or issued under the
authority of a monarch, duke, or count, by himself or with the advice of
his council. Includes early law codes.</td></tr>

<tr><td>Law - Local Ordinances</td><td>Regulations, by-laws, and ordinances issued by a local
authority such as towns and villages.  Can sometimes be embedded in other
records, such as Court Rolls or Custumals.</td></tr>

<tr><td>Law - Treatise/Commentary</td><td>Learned discussions of points of law, interpretations of legislation, such as Bracton, 
Glanvill and others.</td></tr>

<tr><td>Letter</td><td>Correspondence written from one person to another,
although the category also includes letters meant to be read by wide
audiences.  Includes formulary books of sample letters. Letters written as
petitions are classified as <span class=field>Record Type</span>:
<i>Petition</i>.</td></tr>

<tr><td>Literature - Drama</td><td>Scripts of plays, can be either prose or verse,
sometimes consist more of stage directions than spoken text.</td></tr>

<tr><td>Literature - Verse</td><td>Any literary work that has a verse structure (with a
particular metrical structure or rhyming pattern), including verse
chronicles, songs and poetry.  Often overlaps with other record
types.</td></tr>

<tr><td>Literature - Prose</td><td>Any literary work that is written in prose, without a
particular metrical structure or rhyme.  Often overlaps with other record
types.</td></tr>

<tr><td>Liturgy</td><td>Instructions for carrying out public rites of worship
or private devotional practices, including specific music and prayers that
follow a liturgical formula. Also includes calendars of feast days,
prayers, blessings, and instructions for saying mass or other worship
ceremonies.</td></tr>

<tr><td>Memoir</td><td>Texts that include narratives of personal experiences,
accounts of events at which the author was involved and present, personal
accounts of pilgrimages or religious experiences, or any first-hand
account of events. Often overlaps with other record types.</td></tr>

<tr><td>Memoir - Family</td><td>Includes French <i>livres de raison</i> and Italian
<i>ricordi</i> or <i>ricordanze</i>, which are personal diaries that
record both family matters (births, marriages, and deaths of family
members, the selection of godparents) and business matters.  Can also
include English common-place books that record family matters.</td></tr>

<tr><td>Miscellany</td><td>Medieval collections of texts that intentionally
contained diverse and often unrelated writings, such as <i>florilegia</i>
(an anthology or "flowering" of writings). </td></tr>

<tr><td>Monastic Rule</td><td>Monastic rules (such as the Benedictine Rule) as well
as rules for laypeople who wish to lead a religious life, such as
<i>Ancrene Wisse</i>.</td></tr>

<tr><td>Muster</td><td>Records of who is eligible to join the army and what
weapons and equipment they have.</td></tr>

<tr><td>Obituary</td><td>
Text noting a death and summing up the life of someone who died.</td></tr>  


<tr><td>Oration</td><td>Any speech, transcription of a speech, or text that is
written for the purpose of being spoken aloud in public, excluding
sermons.</td></tr>

<tr><td>Other</td><td>Temporarily used as a catch-all term for items that
don.t fit anywhere else.  Most will eventually be reclassified under an
existing or new record type.</td></tr>

<tr><td>Papal Bull</td><td>Formal letters from the pope, sealed with the papal bulla.  They are often included in 
cartularies or as part of ecclesiastical disputes.</td></tr>

<tr><td>Penitential</td><td>
A text of canons or guidelines about sins and the punishments imposed for them</td></tr>


<tr><td>Petition</td><td>A letter written by an individual or a group of
individuals to a higher power asking for them to take some action, usually
to right a wrong.</td></tr>

<tr><td>Philosophic Work</td><td>Any work of a philosophical nature, including
epistemology, logic, and metaphysics.  Can sometimes
overlap with <i>Theology - Doctrine.</i></td></tr>

<tr><td>Prophecy</td><td>A work that purports to predict the future.  Some prophecies focus primarily on political 
events; some focus on religious events; some discuss the millennium or the end of the world.</td></tr>

<tr><td>Proverbs</td><td>Collection of maxims or brief popular epigrams</td></tr>

<tr><td>Register - Bishop</td><td>Records kept by ecclesiastical authorities to record
proceedings of visitations to monastic institutions, ordinations, and
other events which fall under bishops. jurisdiction.</td></tr>

<tr><td>Register - Notarial</td><td>Records kept by notaries recording all of the
documents they witness, such as contracts, charters, and wills.</td></tr>

<tr><td>Register – Other</td><td>
Includes records kept in a register (usually a ledger or book-format) by an authority such as the pope, official of a secular ruler, etc…</td></tr>


<tr><td>Scripture</td><td>Bibles in all languages, Psalters, Books of Hours,
picture bibles, and other biblical texts.</td></tr>

<tr><td>Sermons</td><td>Religious instructional speeches, intended to be
delivered in public, often as part of a worship service.  Also includes
sermons circulated in handbooks or formularies for priests to use as
models, and commemorative orations (<i>panegyrics</i>) or sermons
delivered as eulogies. </td></tr>

<tr><td>Taxes</td><td>Documents relating to the imposition and collection of
taxes.  Includes tolls, which are lists of taxes or customs assessed on
goods coming in and out of a settlement or country (these can often be
part of another record, such as <i>Charters</i> or <i>Law -
Legislation</i>).</td></tr>

<tr><td>Taxes - Clerical </td><td>Documents relating to the imposition and collection of
taxes on clergy. </td></tr>

<tr><td>Taxes - Lay </td><td>Documents relating to the imposition and collection of
taxes on laypeople by the state, town, or local lord.</td></tr>

<tr><td>Taxes - Poll Tax</td><td>Documents relating to the imposition and collection of
"head" taxes, or taxes levied on every individual; mostly relates to
England, 1377-81. </td></tr>

<tr><td>Theology</td><td>Writings that focus on religious doctrine and issues.
[Used only if one of the other Theological categories does not fit.]
</td></tr>

<tr><td>Theology - Devotional</td><td>Guides to pious living, works (such as books of hours,
ladders of salvation) for private devotional purposes, literary works
meant to stimulate religious contemplation.  Can include mystical works
meant to be read during meditation or contemplation.  Penitential works
meant for the clergy are entered under <i>Theology - Practical</i> unless
there
is evidence they were used by lay people for devotional purposes, in which
case they are entered under both Record Types.</td></tr>

<tr><td>Theology - Doctrine</td><td>Theological works written by learned men (and a few
women), including the church fathers, schoolmen, and university masters,
which discuss the principles, laws, and beliefs established by the Church
as official dogma on such matters as the nature of the Trinity, the
sacraments, and salvation. Includes summas, glosses on summas, quodlibets,
and questiones.</td></tr>

<tr><td>Theology - Mystical Work</td><td>Descriptions of mystical visions, treatises about
mysticism, and other works relating to mystical experiences.<br><br></td></tr>

<tr><td>Theology - Practical/Instructional</td><td>Instructions for priests, penitentials (manuals for
priests to use during confession, which lists sins parishioners might
commit and the proper penance for each), and other works (such as ladders
of salvation) meant as prescriptive guides for priests and other religious
leaders.  Such literature could also be read by lay people, in which case
the item is entered under both Record Types.</td></tr>

<tr><td>Theology - Summal</td><td>Systematic overview of the Christian faith written in a scholastic format. It includes a 
systematic arrangement of all possible arguments for a given position and against its contrary. Can be similar to 
a disputation, but in an abbreviated form not tied to an actual oral debate.</td></tr>

<tr><td>Translation</td><td>A work translated from one language to another in the
middle ages.</td></tr>

<tr><td>Treatise - Instruction/Advice</td><td>Cookbooks, grammars, books of courtesy, military
handbooks, and other "how to" works.<br><br></td></tr>

<tr><td>Treatise - Political</td><td>Mirrors for princes, treatises about laws and
governments.  Mirrors for princes can overlap with the <i>Treatises -
Instruction/Advice</i>.</td></tr>

<tr><td>Treatise - Scientific/Medical</td><td>Treatises about medicine, astrology, alchemy, and
other scientific works such as bestiaries and herbals.<br><br></td></tr>

<tr><td>Treatise - Other</td><td>A temporary category for treatises that don't fit
elsewhere.</td></tr>

<tr><td>Treaty/Diplomatic</td><td>
Documents that concern diplomatic relations between different geo-political regions (or within a region in the case of a treaty settling a civil war). Note that such documents will often have more than one Record Type (such as Letter or Treatise-Political).</td></tr>


<tr><td>Visitations</td><td>Records (usually a set of questions followed by the answers of individuals) generated when 
bishops or other higher authorities visited a monastery or other institution to investigate the state of the 
institution or wrongdoing.</td></tr>

<tr><td>Will</td><td>A document stating what should be done with a person's
possessions upon his or her death.</td></tr>


</table>
